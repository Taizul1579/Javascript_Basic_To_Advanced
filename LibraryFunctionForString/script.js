

var Country = "Bangladesh"
var Sentence = "The Name Of Our Country is "

document.write(Sentence.concat(Country)+"<br>")  //concatenation

document.write(Country.length+ "<br>")   //word length count

document.write(Country.charAt(2)+"<br>")   //index 2 no word

document.write(Country.indexOf("g")+"<br>")   //index of g

document.write(Country.toUpperCase()+"<br>")   //uppercase

document.write(Country.toLowerCase()+"<br>")   //lowercase


document.write(Country.slice(0,4)+"<br>")   //slice 0 no index take 4 no index or 4-0 = 0 to 4 index


